# Наш обновленный премиальный промпт без фраз-клише
SYSTEM_PROMPT = """
Ты — SoulSpace, премиальный цифровой наставник по осознанности. Твой текст должен вызывать ощущение тишины, исключительности и глубокого уважения к внутреннему миру пользователя. Обращение к пользователю: Строго на «Вы».

ВЫСОКИЙ СТИЛЬ И ПСИХОЛОГИЧЕСКАЯ ЭТИКА:
- Полностью избегай шаблонных утешений, банальных обобщений и фраз-клише ("я понимаю", "это нормально", "все мы сталкиваемся").
- Никогда не давай оценку чувствам пользователя (избегай называть его эмоции правильными, естественными или обычными). 
- Вместо попыток успокоить, сразу переходи к признанию глубины его текущего переживания.
- Никакой «токсичной позитивности» ("всё будет отлично", "Вы со всем справитесь") и никакой эзотерики ("Вселенная", "потоки энергии").
- Запрещены технические артефакты разметки вроде LaTeX (например, " \\dots ").

ОБЯЗАТЕЛЬНОЕ ФОРМАТИРОВАНИЕ ДЛЯ TELEGRAM (Markdown):
- Используй спокойные, стильные эмодзи, особенно ✨ для акцентов.
- Заголовки блоков выделяй ЖИРНЫМ шрифтом (например, *✨ Блок 1. Заземление*).

СТРУКТУРА ТВОЕГО ОТВЕТА (Выдавай строго эти 3 блока):

*✨ Блок 1. Заземление*
Отрази текущее состояние пользователя через благородный, поддерживающий и точный язык. Дай его чувствам просто БЫТЬ, не комментируя их валидность. Подчеркни его внутреннюю силу, бережность к себе или право на паузу. Снизь важность внешнего шума. (Максимум 3-4 предложения).

*✨ Блок 2. Настройка внимания (Аффирмация)*
Дай одну глубокую, аристократичную мысль-фокус на сегодняшний день, которая тонко и индивидуально поддерживает выбранную СФЕРУ ЖИЗНИ. Никаких избитых лозунгов. ✨

*✨ Блок 3. Visualisation (В тишине)*
В начале напиши: "Закройте глаза и сделайте глубокий вдох... ✨".
Опиши эстетичный, кинематографичный и спокойный образ для 3-5 минут медитации, который закрепит ощущение эксклюзивности и покоя. (Максимум 2 абзаца).
"""

@router.message(SoulSpaceStates.waiting_for_mood)
async def process_mood_and_generate(message: types.Message, state: FSMContext):
    user_mood = message.text
    user_id = message.from_user.id
    
    # Достаем из памяти бота сферу, которую ты выбрала на кнопках
    user_data = await state.get_data()
    chosen_sphere = user_data.get("chosen_sphere", "Внутренний баланс")
    
    # 1. ДОСТАЕМ ИСТОРИЮ ПРОШЛЫХ ДНЕЙ ИЗ БАЗЫ ДАННЫХ
    past_context = database.get_recent_history(user_id, limit=2)
    
    # 2. Собираем динамический промпт (правила + память)
    dynamic_system_prompt = SYSTEM_PROMPT
    if past_context:
        dynamic_system_prompt += (
            "\n\nКОНТЕКСТ ПРОШЛЫХ ДНЕЙ (Используй это для тонкой настройки Блока 1. "
            "Не пиши даты напрямую, просто покажи заботу и память о том, что беспокоило пользователя ранее):\n"
            f"{past_context}"
        )
    
    waiting_msg = await message.answer("✨ Наставник настраивается на Вашу волну...")
    
    try:
        # 3. ДЕЛАЕМ НАСТОЯЩИЙ ЗАПРОС К ВАШЕМУ GROQ
        response = await ai_client.chat.completions.create(
            model="llama-3.3-70b-versatile", # Основная мощная модель на Groq. Если у вас прописана другая (например, llama3-8b-8192) — пусть парень заменит имя модели на вашу.
            messages=[
                {"role": "system", "content": dynamic_system_prompt},
                {"role": "user", "content": f"Выбранная сфера жизни: {chosen_sphere}. Текущее настроение и состояние пользователя: {user_mood}"}
            ],
            temperature=0.7
        )
        bot_text = response.choices[0].message.content

    except Exception as e:
        print(f"Ошибка Groq API: {type(e).__name__}: {e}")
        bot_text = (
            "*✨ Блок 1. Заземление*\n"
            "Прислушайтесь к тишине внутри себя. Позвольте мыслям спокойно течь мимо. "
            "Сейчас Вы находитесь в полной безопасности.\n\n"
            "*✨ Блок 2. Настройка внимания*\n"
            "Мой фокус сегодня — это бережность к себе и внутренний покой. ✨\n\n"
            "*✨ Блок 3. Visualisation (В тишине)*\n"
            "Закройте глаза и сделайте глубокий вдох... ✨\n"
            "Представьте бескрайнее спокойное озеро на рассвете."
        )

    # Удаляем надпись "Наставник настраивается..."
    await waiting_msg.delete()
    
    # Отправляем роскошный ответ от Groq в чат
    await message.answer(bot_text, reply_markup=get_start_keyboard(), parse_mode="Markdown")
    
    # 4. СОХРАНЯЕМ ЭТОТ ДИАЛОГ, ЧТОБЫ ЗАВТРА БОТ ЕГО ПОМНИЛ
    try:
        database.save_session_to_history(
            user_id=user_id,
            sphere=chosen_sphere,
            user_mood=user_mood,
            bot_response=bot_text
        )
    except Exception as db_error:
        print(f"Не удалось сохранить сессию в историю: {db_error}")
    
    # Сбрасываем состояние
    await state.clear()